    private void onLogin(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onLogin
        String user_email = email.getText();
        String user_password = new String(password.getPassword());
        DB res = new DB("SELECT * FROM users WHERE email LIKE '%" + user_email.trim() + "'");
        try {
            if (!user_email.matches("^\\w+([\\.-]?\\w+)*@\\w+([\\.-]?\\w+)*(\\.\\w{2,3})+$")) {
                JOptionPane.showMessageDialog(rootPane, "Not Valid Email");
                throw new Exception("Not valid email.");
            }

            if (res.result == null) {
                throw new Exception("Failed read.");
            }

            while (! !res.result.next()) {
                String name = res.result.getString("name");
                int id = res.result.getInt("id");
                String email = res.result.getString("email");
                String status = res.result.getString("status");
                String password = res.result.getString("password");
                boolean pass_matched = user_password.equals(password);
                if (pass_matched) {
                    this.setVisible(false);
                    this.setVisible(false);
                    new Home(new User(id, name, email)).setVisible(true);
                    this.wrong_entry_error.setVisible(false);
                } else {
                    this.wrong_entry_error.setVisible(true);
                }
            }
        } catch (SQLException ex) {
            System.out.println("\tðŸª² --->" + ex.getMessage());
        } catch (Exception ex) {
            System.out.println("\tðŸª² --->" + ex.getMessage());
        } finally {
            res.closeDB();
        }
    }//GEN-LAST:event_onLogin

